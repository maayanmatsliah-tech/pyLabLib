    _branch_node_name="_branch"
    def collect_nodes(self, node, pfx="", tree=None, add_branch=True):
        if tree is None:
            tree={}
        name=py3.as_str(self.spinNodeGetName(node))
        if pfx:
            name=pfx+"/"+name
        if self.spinNodeGetType(node)==spinNodeType.CategoryNode:
            if add_branch:
                tree[name+"/"+self._branch_node_name]=node
            for i in range(self.spinCategoryGetNumFeatures(node)):
                self.collect_nodes(self.spinCategoryGetFeatureByIndex(node,i),pfx=name,tree=tree,add_branch=add_branch)
        else:
            tree[name]=node
        return tree

