class TeledyneSpinnakerError(TeledyneError):
    """Generic Teledyne Spinnaker error"""


class TeledyneSpinnakerLibError(TeledyneSpinnakerError):
    """Generic Teledyne Spinnaker library error"""
    def __init__(self, func, code, lib=None):
        self.func=func
        self.code=code
        self.name=drspinError.get(code,"N/A")
        self.desc=""
        self.full_desc=""
        try:
            if lib is not None:
                last_code=lib.spinErrorGetLast()
                if last_code==self.code:
                    self.desc=py3.as_str(lib.spinErrorGetLastMessage())
                    self.full_desc=py3.as_str(lib.spinErrorGetLastFullMessage())
        except TeledyneSpinnakerLibError:
            pass
        self.msg="function '{}' raised error {}({}): {}".format(func,code,self.name,self.desc)
        super().__init__(self.msg)
def errcheck(passing=None, lib=None):
    """
    Build an error checking function.

    Return a function which checks return codes of Spinnaker library functions.
    `passing` is a list specifying which return codes are acceptable (by default only 0, which is success code, is acceptable).
    """
    if passing is None:
        passing={0}
    def errchecker(result, func, arguments):  # pylint: disable=unused-argument
        if result not in passing:
            raise TeledyneSpinnakerLibError(func.__name__,result,lib=lib)
        return result
    return errchecker


